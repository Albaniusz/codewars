package com.codewars.kyu8.grasshopper.combineStrings;

/**
 * <h1>Combine strings function</h1>
 * <p>
 * Example:
 * <p>
 * combineNames('James', 'Stevens')
 * <p>
 * returns:
 * <p>
 * 'James Stevens'
 */
public class StringCombiner {
    public static String combineNames(String first, String last) {
        return first + " " + last;
    }
}
