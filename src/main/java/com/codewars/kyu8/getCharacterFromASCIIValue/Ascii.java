package com.codewars.kyu8.getCharacterFromASCIIValue;

/**
 * Write a function which takes a number and returns the corresponding ASCII char for that value.
 * <p>
 * Example:
 * <p>
 * 65 --> 'A'<br/>
 * 97 --> 'a'<br/>
 * 48 --> '0
 * <p>
 * For ASCII table, you can refer to <a href="http://www.asciitable.com/">http://www.asciitable.com/</a>
 */
public class Ascii {
    public static char getChar(int c) {
        return (char) c;
    }
}
